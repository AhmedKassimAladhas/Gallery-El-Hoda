    private void btnsearchdate1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchdate1ActionPerformed
        if (date1.getCalendar() == null) {
            Tools.msgbox("ادخل التاريخ اولا !!");
        } else {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String Date1 = dateFormat.format(date1.getDate());
            java.sql.Date dateOne = java.sql.Date.valueOf(Date1);
            ac.getAccByOneDate(dateOne, jTable);

            String exp = "0";

            String disc = "0";
            String totalBuy = "0";
            String totalSale = "0";

            exp = ac.getAllExpensesOne(dateOne);
            disc = ac.getAllDiscountOne(dateOne);
            totalBuy = ac.getAllTotalBuytOne(dateOne);
            totalSale = ac.getAllTotalSaletOne(dateOne);
            if (exp == null) {
                totalexptxt.setText("0");
            } else {
                totalexptxt.setText(exp);
            }
            if (disc == null) {
                totaldiscounttxt.setText("0");
            } else {
                totaldiscounttxt.setText(disc);
            }
            if (totalBuy == null) {
                totalpricebuytxt.setText("0");
            } else {
                totalpricebuytxt.setText(totalBuy);
            }
            if (totalSale == null) {
                totalpricesaletxt.setText("0");
            } else {
                totalpricesaletxt.setText(totalSale);
            }

            if (totalexptxt.getText().equals("") || totaldiscounttxt.getText().equals("") || totalpricesaletxt.getText().equals("")) {
                totaldaytxt.setText("0");

            } else {
                Double totalDisc = Double.parseDouble(totalexptxt.getText()) + Double.parseDouble(totaldiscounttxt.getText());
                Double total = Double.parseDouble(totalpricesaletxt.getText()) - totalDisc;
                totaldaytxt.setText(total.toString());

                Double profitBefor = Double.parseDouble(totalpricesaletxt.getText()) - Double.parseDouble(totalpricebuytxt.getText());
                totalProfitbefortxt.setText(profitBefor.toString());

                Double profitAfter = profitBefor - totalDisc;
                totalProfitaftertxt.setText(profitAfter.toString());
            }
        }
    }//GEN-LAST:event_btnsearchdate1ActionPerformed

