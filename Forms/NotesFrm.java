    private void searchallbtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchallbtn1ActionPerformed
        if (datefrom.getCalendar() != null) {
            if (dateto.getCalendar() == null) {
                SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
                String df = f.format(datefrom.getDate());
                n.getBillByOneDate(df, payment, jTable1);
            } else {
                SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
                String dfr = f.format(datefrom.getDate());
                String dt = f.format(dateto.getDate());
                n.getBillByManyDate(dfr, dt, payment, jTable1);
            }
        } else {
            Tools.msgbox("ادخل التاريخ اولا");
        }
    }//GEN-LAST:event_searchallbtn1ActionPerformed

