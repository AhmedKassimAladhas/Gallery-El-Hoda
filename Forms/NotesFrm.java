    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            int row = jTable1.getSelectedRow();
            String notes = jTable1.getValueAt(row, 0).toString();
            String pay = jTable1.getValueAt(row, 1).toString();
            String phone = jTable1.getValueAt(row, 2).toString();
            String cost = jTable1.getValueAt(row, 3).toString();
            String name = jTable1.getValueAt(row, 4).toString();
            SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
            Date date = f.parse(jTable1.getValueAt(row, 5).toString());
            String id = jTable1.getValueAt(row, 6).toString();

            notestxt.setText(notes);
            phonetxt.setText(phone);
            costtxt.setText(cost);
            nametxt.setText(name);
            jDateChooser1.setDate(date);
            idtxt.setText(id);

            updatebtn.setEnabled(true);
            deletebtn.setEnabled(true);
            savebtn.setEnabled(false);
            if (pay.equals("لم يسدد")) {
                paymentbtn.setEnabled(true);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTable1MouseClicked

