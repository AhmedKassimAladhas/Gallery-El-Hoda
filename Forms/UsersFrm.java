    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        if (idtxt.getText().equals("") || usernamtxt.getText().equals("") || passwordtxt.getText().equals("")) {
            Tools.msgbox("يجب ادخال البيانات اولا !!");
        } else {
            setValues();
            u.add();
            Tools.clearText(this);
            idtxt.setText(MethodsDB.getAutoNumber("users", "number"));
            u.getAllRows(jTable5);
        }
    }//GEN-LAST:event_savebtnActionPerformed

    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        if (idtxt.getText().equals("") || usernamtxt.getText().equals("") || passwordtxt.getText().equals("")) {
            Tools.msgbox("يجب اختيار اليوزر من الجدول اولا !!");
        } else {
            setValues();
            u.update();
            savebtn.setEnabled(true);
            updatebtn.setEnabled(false);
            deletebtn.setEnabled(false);
            nametxt.requestFocus();
            Tools.clearText(this);
            idtxt.setText(MethodsDB.getAutoNumber("users", "number"));
            u.getAllRows(jTable5);
        }
    }//GEN-LAST:event_updatebtnActionPerformed

    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        boolean ifyes = Tools.confirmMsg("هل تريد حذف اليوزر");
        if (ifyes == true) {
            u.delete(idtxt.getText());
            Tools.clearText(this);
            String id = MethodsDB.getAutoNumber("users", "number");
            idtxt.setText(id);
            u.getAllRows(jTable5);
            savebtn.setEnabled(true);
            updatebtn.setEnabled(false);
            deletebtn.setEnabled(false);
            nametxt.requestFocus();
        } else {
            System.out.println("no deleted");
        }
    }//GEN-LAST:event_deletebtnActionPerformed

