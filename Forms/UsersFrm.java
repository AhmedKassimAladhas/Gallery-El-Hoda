    private void jTable5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable5MouseClicked
        try {
            int row = jTable5.getSelectedRow();
            String pass = jTable5.getValueAt(row, 0).toString();
            String user = jTable5.getValueAt(row, 1).toString();
            String nam = jTable5.getValueAt(row, 2).toString();
            String eid = jTable5.getValueAt(row, 3).toString();

            nametxt.setText(nam);
            usernamtxt.setText(user);
            passwordtxt.setText(pass);
            idtxt.setText(eid);

            savebtn.setEnabled(false);
            updatebtn.setEnabled(true);
            deletebtn.setEnabled(true);
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_jTable5MouseClicked

